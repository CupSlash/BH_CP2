#function for drawing a triangle in turtle graphics
def draw_triangle():
    turtle = Turtle
    screensize(200,200)
    fillcolor()
    begin_fill()
    speed(0)
    tracer(0)
    hideturtle()
    penup()
    goto(200, 200)
    pendown()
    forward(200)
    left(60)
    forward(200)
    penup()
    end_fill()
    done()

